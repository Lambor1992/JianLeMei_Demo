package com.jlm.app.jianlemei_demo.db.health;

public class Accompany {
    private Integer accompanyId;
    private String accompanyName;

    public Accompany() {
    }

    public Integer getAccompanyId() {
        return this.accompanyId;
    }

    public void setAccompanyId(Integer accompanyId) {
        this.accompanyId = accompanyId;
    }

    public String getAccompanyName() {
        return this.accompanyName;
    }

    public void setAccompanyName(String accompanyName) {
        this.accompanyName = accompanyName;
    }
}